    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        if (jFormattedTextField1.getText().length()<10 || !jFormattedTextField1.isEditValid()) {
            JOptionPane.showMessageDialog(null,"Ingrese un numero de telefono valido (10 digitos)");
            return;
        }
        
        if (jTextField1.getText().isBlank() || jTextField2.getText().isBlank() || jFormattedTextField1.getText().isBlank()) {
            JOptionPane.showMessageDialog(null,"Ingrese la informacion solicitada");
            return;
        }
        
        String[] datos = new String[4];
        datos[0] = jTextField1.getText();
        datos[1] = jTextField2.getText();
        datos[2] = jFormattedTextField1.getText();
        datos[3] = jComboBox1.getSelectedItem().toString();
        modelo.addRow(datos);
        
        jTextField1.setText("");
        jTextField2.setText("");
        
        jFormattedTextField1.setFocusLostBehavior(COMMIT);
        jFormattedTextField1.setText("");
        
    }//GEN-LAST:event_jButton1MouseClicked

